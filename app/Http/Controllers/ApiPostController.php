     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $post = new Post;
        $post->titolo = $request->titolo;
        $post->descrizione = $request->descrizione;
        $post->user_id = Auth::id();
        $post->save();
        return response('success', 201);
    }

